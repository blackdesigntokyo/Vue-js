/*
1. Vue.createApp({ ... })
これはVueの新しいアプリケーションインスタンスを作成する部分です。波括弧 {} の中には、そのアプリケーションの振る舞いを定義する「オプション」を記述します。

オプション名:
data: アプリケーションのデータを定義するためのオプションです。HTMLテンプレート内で利用したい変数をここに記述します。

値:
function() { ... }: dataオプションの値は必ず関数である必要があります。これは、各コンポーネントが独立したデータを持つようにするためです。
return { ... };: この関数の戻り値として、message: '皆さん、こんにちは！'のようなオブジェクトを返します。このオブジェクトのキー（message）が変数名、値（'皆さん、こんにちは！'）がその変数に代入される実際のデータになります。

2. .mount('#app')
これは作成したVueアプリケーションを、指定したHTML要素に適用する部分です。

適用する要素:
#app: これはCSSセレクタです。この場合、IDがappであるHTML要素に、Vueアプリケーションを適用します。具体的には、<div id="app">のような要素です。
*/